public class Gorevli {
    private String ad;
    private String soyad;
    private int tc;

    public Gorevli(String ad, String soyad, int tc) {
        this.ad = ad;
        this.soyad = soyad;
        this.tc = tc;
    }

    public String getAd() {
        return ad;
    }

    public String getSoyad() {
        return soyad;
    }

    public int getTc() {
        return tc;
    }
}
